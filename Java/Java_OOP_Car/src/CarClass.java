public class CarClass {
    String Marka;
    String Model;

    public CarClass(String marka, String model) {
        this.Marka = marka;
        this.Model = model;
    }

    public String getMarka() {
        return this.Marka;
    }

    public void setMarka(String marka) {
        this.Marka = marka;
    }

    public String getModel() {
        return this.Model;
    }

    public void setModel(String model) {
        this.Model = model;
    }
}


